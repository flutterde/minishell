#include "../../minishell.h"

void	print_lexer(t_lex *lex)
{
	printf("-------------------------------------------------------------------------\n");
	printf("|	content		|	state	|	len	|	token	|\n");
	printf("-------------------------------------------------------------------------\n");
	while (lex)
	{
		printf("|	'%s'		|	%d	|	%d	|	%d	|\n",
			lex->string, lex->state, lex->len, lex->type);
		lex = (lex)->next;
	}
	printf("-------------------------------------------------------------------------\n");
}
